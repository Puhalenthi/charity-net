export type TagVocabulary = (typeof TAG_VOCABULARY)[number];

const tagSet = new Set<string>(TAG_VOCABULARY);

export function isVocabTag(value: string): value is TagVocabulary {
  return tagSet.has(value);
}

export function normalizeTag(value: string): string {
  return value.trim().toLowerCase().replace(/\s+/g, '-');
}

export function filterToVocab(values: readonly string[]): TagVocabulary[] {
  const out: TagVocabulary[] = [];
  const seen = new Set<string>();
  for (const v of values) {
    const n = normalizeTag(v);
    if (!seen.has(n) && isVocabTag(n)) {
      seen.add(n);
      out.push(n);
    }
  }
  return out;
}
